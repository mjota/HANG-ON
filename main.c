void main(){
	int i;
	initlcd();
	//Bring Cursor to Line 1, character 1
	cursorxy(1,1);
	//Print string to LCD
	putstr("Welcome to Rickey");

	//Bring pointer to 0,0 pixel position
	pixelxy(0,0);
	//Write gfx data on LCD
	for(i=0;i<504;i++)
		wrdata(0X00);
	pixelxy(0,0);
	wrdata(0xFF);
	pixelxy(1,1); //0 Columna, 1 Fila
	wrdata(0xFF);
	pixelxy(2,2); //0 Columna, 1 Fila
	wrdata(0xFF);
	pixelxy(3,3); //0 Columna, 1 Fila
	wrdata(0xFF);
	pixelxy(4,4); //0 Columna, 1 Fila
	wrdata(0xFF);
	pixelxy(5,5); //0 Columna, 1 Fila
	wrdata(0xFF);
	
	
	while(1){
		if(AC_DR==0)
			LED = 0;
		else
			LED = 1;

		if(AC_ES==0)
			BUZ = 0;
		else
			BUZ = 1;

		if(BUT_ES==0)
			putstr("-->");
		
		if(BUT_DR==0)
			putstr("  ");

		if(BUT_AC==0)
			putstr(" ^^ ^^ ");



	}
}
