void intro(){
	int i;
	initlcd();
	//Print string to LCD
	//putstr("Welcome to Rickey");

	pixelxy(0,0);
	for(i=0;i<504;i++)
		wrdata(sega[i]);
		
	pixelxy(14,4);
	for(i=0;i<54;i++)
		wrdata(presents[i]);
}

void initgame(){
	int i;
	pixelxy(0,0);
	for(i=0;i<504;i++)
		wrdata(0x00);
	
	pixelxy(40,5);
	for(i=0;i<4;i++)
		wrdata(0x0F);
	
	poscar = 40;
}

void changecar(){
	int i;
	unsigned int posexa,posexp;
	
	posexa = poscar - 1;
	pixelxy(posexa,5);
	wrdata(0x00);
	
	posexp = poscar + 5;
	pixelxy(posexp,5);
	wrdata(0x00);
	
	pixelxy(poscar,5);
	for(i=0;i<4;i++)
		wrdata(0x0F);
}

void main(){
	intro();
	
	initgame();
	
	while(1){
		if(AC_DR==0)
			LED = 0;
		else
			LED = 1;

		if(AC_ES==0)
			BUZ = 0;
		else
			BUZ = 1;

		if(BUT_ES==0){
			poscar += 1;
			changecar();
		}
		
		if(BUT_DR==0){
			poscar -= 1;
			changecar();
		}
		
		if(BUT_AC==0)
			putstr(" ^^ ^^ ");



	}
}
