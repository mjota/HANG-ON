	pixelxy(40,5);
	for(i=0;i<4;i++)
		wrdata(0x0F);
	
	cursorxy(1,1);
	putstr("00:00");	
	cursorxy(1,12);
	putstr("1");
	pixelxy(75,0);
	for (i=0;i<8;i++){
		wrdata(heart[i]);
	}
	pixelxy(0,1);
	for (i=0;i<84;i++)
		wrdata(0x01);	
	
	poscar = 40;
	posxobs = numrandom(42);
	//posxlife = numrandom(75);
